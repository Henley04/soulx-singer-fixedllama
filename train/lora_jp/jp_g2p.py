"""
Python port of the Japanese G2P (grapheme-to-phoneme) logic from
src/inference/pipeline/textProcessing.js (_japaneseG2p).

This module is the SINGLE SOURCE OF TRUTH for Japanese lyric → phoneme
conversion on the Python training side. It MUST produce byte-identical
output to the JS inference-side _japaneseG2p, otherwise training and
inference phoneme token structures will diverge (the root cause of the
'watashino → woc(su)shibi' misalignment bug documented in project_memory).

Input: hiragana / katakana / kanji (with <jp> prefix) / romaji letters.
Output: list of bare phoneme strings (e.g. ['k', 'a', 'ts', 'u']).

Usage:
    from train.lora_jp.jp_g2p import japanese_g2p, syllabify_jp
    phonemes = japanese_g2p("かたつむり")  # ['k','a','t','a','ts','u','m','u','r','i']
    syllables = syllabify_jp(phonemes)     # [['k','a'],['t','a'],['ts','u'],['m','u'],['r','i']]
"""

