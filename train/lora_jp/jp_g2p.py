JP_CONSONANTS = {
    'k', 's', 't', 'n', 'h', 'm', 'r', 'w', 'y', 'g', 'z', 'd', 'b', 'p',
    'f', 'j', 'ch', 'sh', 'ts', 'ky', 'gy', 'ny', 'hy', 'my', 'ry', 'py', 'by', 'cl',
}
JP_VOWELS = {'a', 'i', 'u', 'e', 'o'}


def is_japanese(text: str) -> bool:
    """Return True if text contains hiragana or katakana (NOT kanji).

    Mirrors _isJapanese in textProcessing.js (line 257-260).
    Kanji is deliberately excluded because it overlaps with Chinese.
    """
    return bool(_JP_KANA_RE.search(text))


def japanese_g2p(text: str) -> List[str]:
    """Convert Japanese text to a flat list of phoneme strings.

    EXACT Python port of _japaneseG2p in textProcessing.js (lines 262-306).
    Output is a list of bare phonemes (no jp_ prefix), e.g. ['k','a','ts','u'].

    Rules (in order, matching JS):
      1. Skip 'ー' (long vowel) and '〜' (wave dash).
      2. Try 2-char combo (yoon like きゃ) in hiragana/katakana maps first.
      3. Try single char in hiragana/katakana maps.
      4. Kanji: try 4→2 char compounds in JP_KANJI_DICT, then single char,
         else emit 'pau'.
      5. Latin letters [a-zA-Z]: emit lowercase single char.
      6. Everything else: skip.
    """
    result: List[str] = []
    i = 0
    n = len(text)
    while i < n:
        ch = text[i]

        # Rule 1: skip long-vowel mark and wave dash
        if ch == 'ー' or ch == '〜':
            i += 1
            continue

        # Rule 2: 2-char combo (yoon)
        if i + 1 < n:
            combo = ch + text[i + 1]
            ph = JP_HIRAGANA_MAP.get(combo) or JP_KATAKANA_MAP.get(combo)
            if ph is not None:
                result.extend(ph.split(' '))
                i += 2
                continue

        # Rule 3: single char
        ph = JP_HIRAGANA_MAP.get(ch) or JP_KATAKANA_MAP.get(ch)
        if ph is not None:
            result.extend(ph.split(' '))
            i += 1
            continue

        # Rule 4: kanji
        if _JP_KANJI_RE.match(ch):
            found = False
            # Try compounds of length 4 down to 2
            max_len = min(4, n - i)
            for length in range(max_len, 1, -1):
                compound = text[i:i + length]
                if compound in JP_KANJI_DICT:
                    result.extend(JP_KANJI_DICT[compound].split(' '))
                    i += length
                    found = True
                    break
            if not found:
                kanji_ph = JP_KANJI_DICT.get(ch)
                if kanji_ph is not None:
                    result.extend(kanji_ph.split(' '))
                else:
                    result.append('pau')
                i += 1
            continue

        # Rule 5: Latin letter
        if _LATIN_RE.match(ch):
            result.append(ch.lower())
            i += 1
            continue

        # Rule 6: skip everything else
        i += 1

    return result


def syllabify_jp(phonemes: List[str]) -> List[List[str]]:
    """Group a flat phoneme list into Japanese syllable units.

    A syllable = optional consonant (or consonant cluster) + one vowel.
    The sokuon 'cl' is a standalone syllable. 'pau' and 'n' (moraic nasal)
    are standalone syllables.

    This mirrors the syllable-splitting logic in prepare_dataset.py's
    split_lab_into_syllables(), but operates on G2P output instead of lab
    segments. The result is used to build the merged 'jp_t-a' format that
    DataProcessor.preprocess() expects (one syllable per MIDI note).

    Examples:
        ['k','a','t','a','ts','u'] -> [['k','a'],['t','a'],['ts','u']]
        ['d','e','n','d','e','n']  -> [['d','e'],['n'],['d','e'],['n']]
        ['ts','u','n','o']         -> [['ts','u'],['n'],['n','o']]
    """
    syllables: List[List[str]] = []
    i = 0
    n = len(phonemes)
    while i < n:
        ph = phonemes[i]

        # Sokuon (促音) → standalone
        if ph == 'cl':
            syllables.append([ph])
            i += 1
            continue

        # Moraic nasal ん → standalone syllable
        if ph == 'n':
            # Check if followed by a vowel (shouldn't happen in valid JP G2P,
            # but handle gracefully) — if so, treat as consonant.
            if i + 1 < n and phonemes[i + 1] in JP_VOWELS:
                pass  # fall through to consonant handling
            else:
                syllables.append([ph])
                i += 1
                continue

        # 'pau' → standalone
        if ph == 'pau':
            syllables.append([ph])
            i += 1
            continue

        # Vowel-only syllable
        if ph in JP_VOWELS:
            syllables.append([ph])
            i += 1
            continue

        # Consonant: collect cluster + following vowel if present
        if ph in JP_CONSONANTS:
            consonant = ph
            if i + 1 < n and phonemes[i + 1] in JP_VOWELS:
                vowel = phonemes[i + 1]
                syllables.append([consonant, vowel])
                i += 2
            else:
                # Consonant without following vowel (rare) — standalone
                syllables.append([consonant])
                i += 1
            continue

        # Unknown phoneme — standalone (shouldn't happen with valid G2P output)
        syllables.append([ph])
        i += 1

    return syllables


def lyrics_to_jp_tokens(lyrics: str) -> List[str]:
    """Full pipeline: lyrics text → merged jp_* tokens (e.g. 'jp_t-a').

    This produces the exact token format that prepare_dataset.py emits and
    that DataProcessor.preprocess() consumes. Each token corresponds to one
    syllable (one MIDI note at inference time).

    Example:
        "かたつむり" → ['jp_k-a','jp_t-a','jp_ts-u','jp_m-u','jp_r-i']
    """
    phonemes = japanese_g2p(lyrics)
    syllables = syllabify_jp(phonemes)
    tokens = []
    for syl in syllables:
        if syl == ['pau']:
            tokens.append('<SP>')
            continue
        if syl == ['cl']:
            tokens.append('jp_cl')
            continue
        # Build 'jp_' + consonant + '-' + vowel (or just 'jp_vowel' for V-only)
        parts = ['jp_' + p for p in syl]
        # Merge into single token: 'jp_t-a' style
        merged = 'jp_' + '-'.join(p[3:] for p in parts)
        tokens.append(merged)
    return tokens


