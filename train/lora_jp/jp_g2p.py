# Regex patterns (mirror textProcessing.js)
_JP_KANA_RE = re.compile(r'[ぁ-ゟァ-ヿ]')          # _isJapanese
_JP_KANJI_RE = re.compile(r'[一-鿿]')              # kanji range in _japaneseG2p
_LATIN_RE = re.compile(r'[a-zA-Z]')

# Consonant set used by prepare_dataset.py (must match PHONEME_MAP consonants)
